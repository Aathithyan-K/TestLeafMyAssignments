package week1.day1;

public class Task3_OddOrEven {

	public static void main(String[] args) {
	
		int givenNumber = 15;
		
		if(givenNumber%2 == 0) 
		{
			System.out.println("The given number is " + givenNumber + ". It is Even");
		}
		else
		{
			System.out.println("The given number is " + givenNumber + ". It is Odd");
		}
	}

}
